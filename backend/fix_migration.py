header = parts[0]
rest = parts[1]

upgrade_downgrade = re.split(r'def downgrade\(\) -> None:', rest)
upgrade_content = upgrade_downgrade[0]
downgrade_content = downgrade_downgrade = upgrade_downgrade[1]

